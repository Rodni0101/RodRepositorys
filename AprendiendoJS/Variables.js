//var
console.log("--Uso de var--");
var holamundo = ("Hola JS");

console.log(holamundo);

holamundo = ("Adios JS");

console.log(holamundo);
console.log();

//let
console.log("--Uso de let--");
let holamundo2 = ("Muy buenas JS");

console.log(holamundo2);

holamundo2 = ("Que pasa JS");

console.log(holamundo2);
console.log();

//const
console.log("--Uso de const--");
const holamundo3 = ("Muy buenas JS");

console.log(holamundo3);

//Var y Let puede variar y const es constante y no se puede cambiar